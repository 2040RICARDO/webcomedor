                        $('.estadoo').html('<div class="alert alert-success" role="alert" ><button class="close" type="button" data-dismiss="alert" aria-hidden="true">x</button><center><strong>Su C.I. no se encuentra registrado en la base de datos</strong></center></div>');
                      }

                     
                      
                      $('#observados').val(""); 
                    },
              });

  });

