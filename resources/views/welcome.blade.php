  $(document).on("submit",".frmeventual",function(e){

    e.preventDefault();
        $('html, body').animate({scrollTop: '0px'}, 200);
    
        var formu=new FormData($(".frmeventual")[0]);
        
        var quien=$(this).attr("id");
        if(quien=="f_asistencia_eventual"){ var varurl="verificaobservado";var token = $("#token").val();
          $('.estadoo').html('<div class="alert " role="alert" ><center><strong>Realizando consulta.....</strong></center></div>');
         }
        $.ajax({
                    url : varurl,
                    headers:{'X-CSRF-TOKEN':token},
                    type: "POST",
                    datatype:'json',
                    data : formu,
                    contentType: false,
                    processData: false,
               success: function(data)
                    {
                      if(data == 1){
                        $('.estadoo').html('<div class="alert alert-danger" role="alert" ><button class="close" type="button" data-dismiss="alert" aria-hidden="true">x</button><center><strong>Usted se encuentra observado, aproximese a la direccion de Bienestar Social</strong></center></div>');
